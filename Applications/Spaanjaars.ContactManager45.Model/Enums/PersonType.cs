namespace Spaanjaars.ContactManager45.Model
{
  /// <summary>
  /// Determines the type of a contact person.
  /// </summary>
  public enum PersonType
  {
    /// <summary>
    /// Indicates an unidentified value.
    /// </summary>
    None = 0,

    /// <summary>
    /// Indicates a friend.
    /// </summary>
    Friend = 1,

    /// <summary>
    /// Indicates a family member.
    /// </summary>
    Family = 2,

    /// <summary>
    /// Indicates a coworker.
    /// </summary>
    Colleague = 3
  }
}
